static char const* u8c_get_name(uint_least32_t code_point);

size_t u8c_unicode_name(char* const restrict buffer, uint_least32_t const code_point) {
	char const* const restrict string = u8c_get_name(code_point);

	size_t const length = strlen(string);

	if (buffer == NULL) { goto return_length; }

	memcpy(buffer, string, length);

return_length:;
	return length;
}

